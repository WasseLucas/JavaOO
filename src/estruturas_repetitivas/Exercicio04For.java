package estruturas_repetitivas;

public class Exercicio04For {
    public static void main(String[] args) {

        int x = 4;
        int y = 0;

        for (int i = 0; i < x; i++){
            System.out.print(i + " ");
            System.out.println(x);
            y = y + 10;
        }
    }
}
